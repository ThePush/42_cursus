#include "libft.h"

char	*ft_strdup(const char *s)
{
	char		*dest;
	size_t		length;
	size_t		i;

	i = 0;
	length = ft_strlen(s);
	dest = malloc((length + 1) * sizeof(char));
	if (!dest)
		return (NULL);
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
