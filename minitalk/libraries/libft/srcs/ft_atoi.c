#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	int		nb;
	int		sign;

	i = 0;
	nb = 0;
	sign = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		(nb = (nb * 10) + str[i] - 48);
		i++;
	}
	return (nb * sign);
}
