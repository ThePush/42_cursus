#include "libft.h"

void	ft_free_2d_array(char ***m)
{
	int	i;

	i = 0;
	while (m && m[0] && m[0][i])
	{
		free(m[0][i]);
		m[0][i] = NULL;
		i++;
	}
	if (m)
	{
		free(m[0]);
		*m = NULL;
	}
}
